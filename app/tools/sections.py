from __future__ import annotations

from dataclasses import dataclass


@dataclass(frozen=True)
class SteelSection:
    """Properties of a standard steel section."""
    name: str
    weight_kg_per_m: float
    area_m2: float
    d_mm: float          # depth
    bf_mm: float         # flange width
    tf_mm: float         # flange thickness
    tw_mm: float         # web thickness
    ix_m4: float         # strong-axis moment of inertia
    iy_m4: float         # weak-axis moment of inertia
    sx_m3: float         # strong-axis section modulus
    sy_m3: float         # weak-axis section modulus
    zx_m3: float         # strong-axis plastic section modulus
    zy_m3: float         # weak-axis plastic section modulus
    rx_m: float          # strong-axis radius of gyration
    ry_m: float          # weak-axis radius of gyration


