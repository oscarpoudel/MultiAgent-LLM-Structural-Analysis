def get_section(name: str) -> SteelSection | None:
    """Look up a section by name (case-insensitive). Searches W, HSS, and Angle sections."""
    key = name.upper().replace(" ", "").replace("×", "X")
    return _W_SHAPES.get(key) or _HSS_SECTIONS.get(key) or _ANGLE_SECTIONS.get(key)


def list_sections(section_type: str = "all") -> list[str]:
    """Return available section names."""
    result = []
    t = section_type.lower()
    if t in ("all", "w"):
        result.extend(sorted(_W_SHAPES.keys()))
    if t in ("all", "hss"):
        result.extend(sorted(_HSS_SECTIONS.keys()))
    if t in ("all", "angle", "l"):
        result.extend(sorted(_ANGLE_SECTIONS.keys()))
    return result


def search_sections(query: str) -> list[SteelSection]:
    """Search sections by partial name match."""
    query = query.upper().replace(" ", "").replace("×", "X")
    results = []
    for db in (_W_SHAPES, _HSS_SECTIONS, _ANGLE_SECTIONS):
        for name, section in db.items():
            if query in name:
                results.append(section)
    return results


def section_to_dict(section: SteelSection) -> dict:
    """Convert a SteelSection to a dictionary for API responses."""
    return {
        "name": section.name,
        "weight_kg_per_m": section.weight_kg_per_m,
        "area_m2": section.area_m2,
        "depth_mm": section.d_mm,
        "flange_width_mm": section.bf_mm,
        "flange_thickness_mm": section.tf_mm,
        "web_thickness_mm": section.tw_mm,
        "Ix_m4": section.ix_m4,
        "Iy_m4": section.iy_m4,
        "Sx_m3": section.sx_m3,
        "Sy_m3": section.sy_m3,
        "Zx_m3": section.zx_m3,
        "Zy_m3": section.zy_m3,
        "rx_m": section.rx_m,
        "ry_m": section.ry_m,
    }
